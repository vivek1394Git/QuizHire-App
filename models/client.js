const mongoose = require("mongoose");

// Schema
const Schema = mongoose.Schema;
const ClientSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  company: {
    type: String,
    required: true,
  },
  designation: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  isAdmin: {
    type: Boolean,
    default: false,
  },
});

// Model
const Client = mongoose.model("Client", ClientSchema);

module.exports = Client;
